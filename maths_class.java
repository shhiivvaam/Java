public class maths_class {
    public static void main(String[] args)
    {
        int a = 10, b = 20;
        System.out.println(Math.max(a,b));

        final int c = Math.max(a,b);
        System.out.println(c);
        
        final int d = Math.min(a,b);
        System.out.println(d);
        
    }
}
