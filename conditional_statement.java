public class conditional_statement {
    public static void main(String[] args)
    {
        boolean sun = true;

        if(sun == true){
            System.out.println("day");
        }
        else{
            System.out.println("night");
        }

        int age = 30;

        if(age>18){
            System.out.println("can vote");
        }
        else{
            System.out.println("cannot vote");
        }
    }
}
